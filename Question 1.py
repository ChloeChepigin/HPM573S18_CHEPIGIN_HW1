
x = 17
print ('Setting x =', x)

# integer
y1 = x
print (type(y1))
print ('y1 is a', type(y1))

# float
y2 = float(x)
print (type(y2))
print ('y2 is a', type(y2))

# string
y3 = str(x)
print ('y3 is a ', type(y3))
print ('y3 is a', type(y3))

# boolean, 17 > 2
y4 = bool(x > 2)
print ('y4 is a ', type(y4))
